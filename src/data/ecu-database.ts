// Busca motos por termo
export function searchMotos(term: string): MotoECU[] {
  const lower = term.toLowerCase();
  return motosECU.filter(
    (m) =>
      m.marca.toLowerCase().includes(lower) ||
      m.modelo.toLowerCase().includes(lower) ||
      m.ano.includes(lower) ||
      `${m.marca} ${m.modelo}`.toLowerCase().includes(lower)
  );
}

// Busca moto por ID
export function getMotoById(id: string): MotoECU | undefined {
  return motosECU.find((m) => m.id === id);
}

// Diagnóstico por sintoma
export function diagnosticarPorSintoma(motoId: string, sintomaTexto: string): DiagnosticSymptom[] {
  const moto = getMotoById(motoId);
  if (!moto) return [];

  const lower = sintomaTexto.toLowerCase();
  return moto.sintomas.filter((s) =>
    s.keywords.some((kw) => lower.includes(kw))
  );
}
